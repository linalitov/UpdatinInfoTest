package Steps;

public class User {
    public int id;
    public String name;
    public String username;
    public String email;
    public String address;
    public String phone;


    public User(String name, String username, String email, String address, String phone) {
        this.id = id;
        this.name = name;
        this.username = username;
        this.email = email;
        this.address = address;
        this.phone = phone;
    }

}
